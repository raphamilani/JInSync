package br.com.jinsync.model;

public class Copybook {

	private String nameCopybook;
	private Integer seqField;
	private String lvlField;
	private String nameField;
	private String redefField;
	private String typeField;
	private String lenIntField;
	private String lenDecField;
	private String posStartField;
	private Integer lenTotField;
	public String getNameCopybook() {
		return nameCopybook;
	}
	public void setNameCopybook(String nameCopybook) {
		this.nameCopybook = nameCopybook;
	}
	public Integer getSeqField() {
		return seqField;
	}
	public void setSeqField(Integer seqField) {
		this.seqField = seqField;
	}
	public String getLvlField() {
		return lvlField;
	}
	public void setLvlField(String lvlField) {
		this.lvlField = lvlField;
	}
	public String getNameField() {
		return nameField;
	}
	public void setNameField(String nameField) {
		this.nameField = nameField;
	}
	public String getRedefField() {
		return redefField;
	}
	public void setRedefField(String redefField) {
		this.redefField = redefField;
	}
	public String getTypeField() {
		return typeField;
	}
	public void setTypeField(String typeField) {
		this.typeField = typeField;
	}
	public String getLenIntField() {
		return lenIntField;
	}
	public void setLenIntField(String lenIntField) {
		this.lenIntField = lenIntField;
	}
	public String getLenDecField() {
		return lenDecField;
	}
	public void setLenDecField(String lenDecField) {
		this.lenDecField = lenDecField;
	}
	public String getPosStartField() {
		return posStartField;
	}
	public void setPosStartField(String posStartField) {
		this.posStartField = posStartField;
	}
	public Integer getLenTotField() {
		return lenTotField;
	}
	public void setLenTotField(Integer lenTotField) {
		this.lenTotField = lenTotField;
	}
	
		
}
